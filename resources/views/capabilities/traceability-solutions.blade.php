                        isClicked = !isClicked;
                        menu.classList.toggle('hidden', !isClicked);
                        button.setAttribute('aria-expanded', isClicked);
                        const svg = button.querySelector('svg');
                        svg.classList.toggle('rotate-180', isClicked);
                        dropdowns.forEach(other => {
