            try {
                const res = await fetch(file);
                if (!res.ok) throw new Error(`Failed to fetch ${file}`);
                const html = await res.text();
                document.getElementById(id).innerHTML = html;
            } catch (error) {
                console.error(`Error loading ${file}:`, error);
            }
        }

