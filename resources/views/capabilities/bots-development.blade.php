                button.addEventListener('click', (e) => {
                    e.stopPropagation(); // Prevent click from closing immediately
                    const dropdownId = button.getAttribute('aria-controls');
                    const dropdown = document.getElementById(dropdownId);
                    const isExpanded = button.getAttribute('aria-expanded') === 'true';

                    // Close all open dropdowns
                    document.querySelectorAll('div[id^="desktop-dropdown-"]').forEach(d => {
                        d.classList.add('hidden');
                    });
                    document.querySelectorAll('button[aria-controls^="desktop-dropdown-"]').forEach(b => {
                        b.setAttribute('aria-expanded', 'false');
                    });

                    // Toggle current dropdown
                    if (!isExpanded) {
                        dropdown.classList.remove('hidden');
                        button.setAttribute('aria-expanded', 'true');
                    }
                });
            });

            // Click outside to close all dropdowns
            document.addEventListener('click', () => {
                document.querySelectorAll('div[id^="desktop-dropdown-"]').forEach(d => d.classList.add('hidden'));
                document.querySelectorAll('button[aria-controls^="desktop-dropdown-"]').forEach(b => {
                    b.setAttribute('aria-expanded', 'false');
                });
            });


            // Contact Form Submission
