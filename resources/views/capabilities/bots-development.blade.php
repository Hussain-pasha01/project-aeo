                            formStatus.textContent = '';
                            formStatus.classList.remove('text-green-700');
                        }, 3000);
                    } else {
                        formStatus.textContent = 'Please fill out all fields.';
                        formStatus.classList.add('text-red-600');
                        setTimeout(() => {
                            formStatus.textContent = '';
                            formStatus.classList.remove('text-red-600');
                        }, 3000);
                    }
                });
            }

